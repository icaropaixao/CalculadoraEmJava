package aplicação;

public class Numero {
    
    // criando objetos &
    // atributos para armazenar valor
    private int valor;
    public Numero(){
        this.valor = 0;
    }        // setar valor
    public void setValor(int valor){
        this.valor = valor;
    }
    // obter valor do numero
    public int getValor(){
        return this.valor;
    }
}
